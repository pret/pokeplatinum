static void EasyChatWordList_InitGroups(EasyChatWordList *wordList)
{
    for (int i = 0, total = 0; i < EASY_CHAT_GROUP_COUNT; i++) {
        wordList->groupStartIndices[i] = total;
        wordList->groupWordCounts[i] = sGroupData[i].processFunc(wordList, sGroupData[i].words, sGroupData[i].length, &wordList->wordsByGroup[total]);
        total += wordList->groupWordCounts[i];
    }
}

static inline void EasyChatWordList_FlagWordAsAdded(EasyChatWordList *wordList, u32 word)
{
    wordList->addedWordBits[word >> 3] |= (1 << (word & 7));
}

static inline BOOL EasyChatWordList_WasWordAddedToGroup(const EasyChatWordList *wordList, u32 word)
{
    return (wordList->addedWordBits[word >> 3] & (1 << (word & 7))) != 0;
}

static u32 EasyChatWordList_ProcessPokemon(EasyChatWordList *wordList, const u16 *words, u32 length, u16 *outList)
{
    u32 total = 0;
    const Pokedex *pokedex = EasyChatArgs_GetPokedex(wordList->easyChatArgs);

    for (u32 i = 0; i < length; i++) {
        if (Pokedex_HasSeenSpecies(pokedex, words[i])) {
            EasyChatWordList_FlagWordAsAdded(wordList, words[i]);
            *outList++ = words[i];
            total++;
        }
    }

    return total;
}

static u32 EasyChatWordList_ProcessMoves(EasyChatWordList *wordList, const u16 *words, u32 length, u16 *outList)
{
    if (EasyChatArgs_IsGameCompleted(wordList->easyChatArgs)) {
        for (u32 i = 0; i < length; i++) {
            EasyChatWordList_FlagWordAsAdded(wordList, *words);
            *outList++ = *words++;
        }

        return length;
    } else {
        return 0;
    }
}

static u32 EasyChatWordList_ProcessToughWords(EasyChatWordList *wordList, const u16 *words, u32 length, u16 *outList)
{
    const UnlockedEasyChatWords *unlockedWords = EasyChatArgs_GetUnlockedWords(wordList->easyChatArgs);
    u32 i, total;
    u16 baseWord = EasyChatWord_FromBankAndEntry(TEXT_BANK_TOUGH_WORDS, 0);

    for (i = 0, total = 0; i < length; i++) {
        if (EasyChatWords_IsToughWordUnlocked(unlockedWords, words[i] - baseWord)) {
            EasyChatWordList_FlagWordAsAdded(wordList, words[i]);
            *outList++ = words[i];
            total++;
        }
    }

    return total;
}

static u32 EasyChatWordList_ProcessGreetings(EasyChatWordList *wordList, const u16 *words, u32 length, u16 *outList)
{
    const UnlockedEasyChatWords *unlockedWords = EasyChatArgs_GetUnlockedWords(wordList->easyChatArgs);

    u16 baseWord = EasyChatWord_FromBankAndEntry(TEXT_BANK_GREETINGS, 0);
    u16 maxLanguageGreeting = baseWord + Greetings_Text_Hola;
    u32 total = 0;

    for (u32 i = 0; i < length; i++) {
        if (words[i] >= baseWord && words[i] <= maxLanguageGreeting) {
            if (!EasyChatWords_IsGreetingUnlocked(unlockedWords, words[i] - baseWord)) {
                continue;
            }
        }

        EasyChatWordList_FlagWordAsAdded(wordList, words[i]);

        *outList++ = words[i];
        total++;
    }

    return total;
}

static u32 EasyChatWordList_ProcessGeneric(EasyChatWordList *wordList, const u16 *words, u32 length, u16 *outList)
{
    for (u32 i = 0; i < length; i++) {
        if (*words == DUPLICATE_WORDS) {
            words++;
            u32 duplicateCount = *words++;
            *outList = *words;

            while (duplicateCount) {
                EasyChatWordList_FlagWordAsAdded(wordList, *words++);
                duplicateCount--;
            }
        } else {
            EasyChatWordList_FlagWordAsAdded(wordList, *words);
            *outList++ = *words++;
        }
    }

    return length;
}

static void EasyChatWordList_InitAlphabetical(EasyChatWordList *wordList)
{
    int total = 0;

    for (int i = 0; i < NELEMS(sWordsByAlphabet); i++) {
        wordList->letterStartIndices[i] = total;
        wordList->letterWordCounts[i] = EasyChatWordList_ProcessAlphabetical(wordList, sWordsByAlphabet[i], wordList->wordsByLetter, total);
        total += wordList->letterWordCounts[i];
    }
}

static u32 EasyChatWordList_ProcessAlphabetical(EasyChatWordList *wordList, const u16 *words, u16 *outList, u32 outListIndex)
{
    u32 total = 0;

    while (*words != WORD_NONE) {
        if (*words == DUPLICATE_WORDS) {
            words++;
            u32 duplicateCount = *words++;

            for (u32 i = 0; i < duplicateCount; i++) {
                if (EasyChatWordList_WasWordAddedToGroup(wordList, words[i])) {
                    outList[outListIndex++] = words[i];
                    total++;
                    break;
                }
            }

            words += duplicateCount;
        } else {
            if (EasyChatWordList_WasWordAddedToGroup(wordList, *words)) {
                outList[outListIndex++] = *words;
                total++;
            }

            words++;
        }
    }

    return total;
}

